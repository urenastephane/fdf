#include "fdf.h"

void		ft_zoom(t_env *e)
{
	int		x;
	int		y;

	x = 0;
	y = 0;
	while (y < e->y)
	{
		while (x < e->x)
		{
			e->pix[y][x].x = x;
			e->pix[y][x].y = y;
			e->pix[y][x].x *= e->zoom;
			e->pix[y][x].y *= e->zoom;
			x++;
		}
		y++;
		x = 0;
	}
}
